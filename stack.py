
stack = []

# Adição de elementos à pilha: push
stack.append(1)
stack.append(2)
stack.append(3)

print("Pilha: ")
print(stack)

# Remoção de elementos da pilha
print("\nElementos removidos da pilha")
print(stack.pop())
print(stack.pop())
print(stack.pop())

print("\nPilha após remoção:")
print(stack)